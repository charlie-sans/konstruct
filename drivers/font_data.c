// Draw a character using a font
int font_draw_char(const Font* font, int x, int y, char c, uint32_t color, uint32_t bgcolor) {
    if (!font) {
        return 0;
    }

    unsigned char ch = (unsigned char)c;

    // Handle unprintable characters
    if (ch < font->first_char || ch > font->last_char) {
        // Replace with a visible character
        ch = '.';
    }

    // Calculate character index
    int char_index = ch - font->first_char;

    // Draw character pixel by pixel
    for (int j = 0; j < font->height; j++) {
        uint8_t row_bits = font->data[char_index * font->height + j];

        for (int i = 0; i < font->width; i++) {
            // Check if the bit is set
            int bit = (row_bits & (1 << (7 - i))) != 0;

            // Draw the pixel
            uint32_t pixel_color = bit ? color : bgcolor;
            vga_putpixel(x + i, y + j, pixel_color);
        }
    }

    return font->width; // Return width of character drawn
}

// Draw a string using a font
int font_draw_string(const Font* font, int x, int y, const char* str, uint32_t color, uint32_t bgcolor) {
    if (!font || !str) return 0;
    
    int original_x = x;
    
    while (*str) {
        if (*str == '\n') {
            x = original_x;
            y += font->height;
        } else if (*str != '\r') { // Skip carriage returns
            int char_width = font_draw_char(font, x, y, *str, color, bgcolor);
            x += char_width;
        }
        str++;
    }
    
    return x - original_x; // Return width of string drawn
}

// Get the width of a string if it were drawn
int font_get_string_width(const Font* font, const char* str) {
    if (!font || !str) return 0;
    
    int width = 0;
    
    while (*str) {
        if (*str >= font->first_char && *str <= font->last_char) {
            width += font->width;
        }
        str++;
    }
    
    return width;
}

