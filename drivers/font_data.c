// Default font descriptor
const Font default_font = {
    8,        // width - 8 pixels per character
    8,        // height - 8 pixels high
    32,       // first_char - starts at space (ASCII 32)
    126,      // last_char - ends at tilde (ASCII 126)
    default_font_data  // data - points to our embedded font
};

// Return the default system font
const Font* font_get_default(void) {
    return &default_font;
}

// Get a pixel value from the font
int font_get_pixel(const Font* font, char c, int x, int y) {
    if (c < font->first_char || c > font->last_char || 
        x < 0 || x >= font->width || 
        y < 0 || y >= font->height) {
        return 0;
    }
    
    // Calculate the offset in the font data
    int char_offset = (c - font->first_char) * font->height;
    
    // The bits are ordered with MSB (bit 7) as the leftmost pixel
    return (font->data[char_offset + y] & (1 << (7 - x))) != 0;
}

